    ldy # $00

:   lda (ZP_COPY_FROM), y                                               ;$7818
    sta (ZP_COPY_TO), y
    dey 
    bne :-
    inc ZP_COPY_FROM+1
    inc ZP_COPY_TO+1
    dex 
    bne :-
    rts

